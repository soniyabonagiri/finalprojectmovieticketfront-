export const generateSeats = () => {
    const rows = ['A', 'B', 'C', 'D', 'E'];
    const seatsPerRow = 8;
    const seats = [];

    rows.forEach((row) => {
        for (let i = 1; i <= seatsPerRow; i++) {
            seats.push({
                id: `${row}${i}`,
                isBooked: Math.random() < 0.2, // Randomly booked
            });
        }
    });

    return seats;
};



